#define HASH_SIZE 2048 // 哈希表大小

// 简单的哈希函数
uint32_t hash(uint32_t x) {
    return (x ^ (x >> 3) ^ (x << 5)) & (HASH_SIZE - 1); // 取模 HASH_SIZE
}

